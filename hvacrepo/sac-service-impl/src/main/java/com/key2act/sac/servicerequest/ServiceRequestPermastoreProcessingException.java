package com.key2act.sac.servicerequest;

public class ServiceRequestPermastoreProcessingException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 345342394948528730L;

	public ServiceRequestPermastoreProcessingException() {
		super();
	}

	public ServiceRequestPermastoreProcessingException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ServiceRequestPermastoreProcessingException(String message, Throwable cause) {
		super(message, cause);
	}

	public ServiceRequestPermastoreProcessingException(String message) {
		super(message);
	}

	public ServiceRequestPermastoreProcessingException(Throwable cause) {
		super(cause);
	}
	
	

}
