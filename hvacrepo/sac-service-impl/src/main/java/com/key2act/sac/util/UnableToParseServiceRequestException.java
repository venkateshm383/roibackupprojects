package com.key2act.sac.util;

public class UnableToParseServiceRequestException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7512250454032380205L;

	public UnableToParseServiceRequestException() {
		super();
	}

	public UnableToParseServiceRequestException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public UnableToParseServiceRequestException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnableToParseServiceRequestException(String message) {
		super(message);
	}

	public UnableToParseServiceRequestException(Throwable cause) {
		super(cause);
	}
	
	
}
