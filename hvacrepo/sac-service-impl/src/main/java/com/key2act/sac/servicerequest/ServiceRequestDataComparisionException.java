package com.key2act.sac.servicerequest;

public class ServiceRequestDataComparisionException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -5556501670102868704L;

	public ServiceRequestDataComparisionException() {
		super();
	}

	public ServiceRequestDataComparisionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ServiceRequestDataComparisionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ServiceRequestDataComparisionException(String message) {
		super(message);
	}

	public ServiceRequestDataComparisionException(Throwable cause) {
		super(cause);
	}
	
	

}
