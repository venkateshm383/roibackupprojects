package com.key2act.sac.servicerequest.bean;

public class ServiceRequestProcessBeanException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -3264001590806963393L;

	public ServiceRequestProcessBeanException() {
		super();
	}

	public ServiceRequestProcessBeanException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ServiceRequestProcessBeanException(String message, Throwable cause) {
		super(message, cause);
	}

	public ServiceRequestProcessBeanException(String message) {
		super(message);
	}

	public ServiceRequestProcessBeanException(Throwable cause) {
		super(cause);
	}
	
	

}
