package com.key2act.timetracker.builder;

public class GeneratePermaDataException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2719610983588346311L;

	public GeneratePermaDataException() {
		super();
	}

	public GeneratePermaDataException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public GeneratePermaDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public GeneratePermaDataException(String message) {
		super(message);
	}

	public GeneratePermaDataException(Throwable cause) {
		super(cause);
	}

}
