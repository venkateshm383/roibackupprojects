package com.getusroi.wms20.label.bartender.service;

public class BartenderServiceException extends Exception {

	private static final long serialVersionUID = 2337512610859604955L;

	public BartenderServiceException() {
		super();
	}

	public BartenderServiceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BartenderServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public BartenderServiceException(String message) {
		super(message);
	}

	public BartenderServiceException(Throwable cause) {
		super(cause);
	}

	
}
