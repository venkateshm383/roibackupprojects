package com.getusroi.wms20.label.bartender;

public class BartenderConfigLoaderException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public BartenderConfigLoaderException() {
		super();

	}

	public BartenderConfigLoaderException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BartenderConfigLoaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public BartenderConfigLoaderException(String message) {
		super(message);
	}

	public BartenderConfigLoaderException(Throwable cause) {
		super(cause);
	}

}
