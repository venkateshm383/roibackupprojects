package com.getusroi.printservice;

public class PrintNodePropertiesLoadingException extends Exception {

	private static final long serialVersionUID = -1966914820045132623L;

	public PrintNodePropertiesLoadingException() {
		super();
	}

	public PrintNodePropertiesLoadingException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public PrintNodePropertiesLoadingException(String message, Throwable cause) {
		super(message, cause);
	}

	public PrintNodePropertiesLoadingException(String message) {
		super(message);
	}

	public PrintNodePropertiesLoadingException(Throwable cause) {
		super(cause);
	}
	
	

}
