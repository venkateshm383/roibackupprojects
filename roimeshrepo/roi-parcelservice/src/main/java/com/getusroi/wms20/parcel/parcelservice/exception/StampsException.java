package com.getusroi.wms20.parcel.parcelservice.exception;

public class StampsException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public StampsException() {
		super();
	}

	public StampsException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public StampsException(String message, Throwable cause) {
		super(message, cause);
	}

	public StampsException(String message) {
		super(message);
	}

	public StampsException(Throwable cause) {
		super(cause);
	}

}
