package com.getusroi.wms20.parcel.parcelservice.exception;

public class ParcelServiceErrorCodePropertiesReaderException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ParcelServiceErrorCodePropertiesReaderException() {
		super();
	}

	public ParcelServiceErrorCodePropertiesReaderException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ParcelServiceErrorCodePropertiesReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public ParcelServiceErrorCodePropertiesReaderException(String message) {
		super(message);
	}

	public ParcelServiceErrorCodePropertiesReaderException(Throwable cause) {
		super(cause);
	}

}
