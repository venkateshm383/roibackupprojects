package com.getusroi.wms20.label.nicelabel.service;

public class NiceLabelServiceException extends Exception {

	private static final long serialVersionUID = 2337512610859604955L;

	public NiceLabelServiceException() {
		super();
	}

	public NiceLabelServiceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NiceLabelServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public NiceLabelServiceException(String message) {
		super(message);
	}

	public NiceLabelServiceException(Throwable cause) {
		super(cause);
	}

	
}
