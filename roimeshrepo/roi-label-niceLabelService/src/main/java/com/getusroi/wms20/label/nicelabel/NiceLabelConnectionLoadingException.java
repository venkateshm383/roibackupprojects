package com.getusroi.wms20.label.nicelabel;

public class NiceLabelConnectionLoadingException extends Exception{

	private static final long serialVersionUID = 7985152823213790804L;

	public NiceLabelConnectionLoadingException() {
		super();
	}

	public NiceLabelConnectionLoadingException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NiceLabelConnectionLoadingException(String message, Throwable cause) {
		super(message, cause);
	}

	public NiceLabelConnectionLoadingException(String message) {
		super(message);
	}

	public NiceLabelConnectionLoadingException(Throwable cause) {
		super(cause);
	}
	
	

}
