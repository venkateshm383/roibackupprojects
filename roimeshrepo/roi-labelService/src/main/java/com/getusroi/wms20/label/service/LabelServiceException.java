package com.getusroi.wms20.label.service;

public class LabelServiceException extends Exception{

	private static final long serialVersionUID = 6579024736146375060L;

	public LabelServiceException() {
		super();
	}

	public LabelServiceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LabelServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public LabelServiceException(String message) {
		super(message);
	}

	public LabelServiceException(Throwable cause) {
		super(cause);
	}
	
	

}
