package com.getusroi.wms20.label;

public class LabelAttributeLoadingException extends Exception{

	private static final long serialVersionUID = 7165495886465726936L;

	public LabelAttributeLoadingException() {
		super();
	}

	public LabelAttributeLoadingException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LabelAttributeLoadingException(String message, Throwable cause) {
		super(message, cause);
	}

	public LabelAttributeLoadingException(String message) {
		super(message);
	}

	public LabelAttributeLoadingException(Throwable cause) {
		super(cause);
	}
	
	

}
